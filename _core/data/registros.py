from datetime import datetime, timedelta


def getTime(excel_time):
    # Convert the Excel time to a timedelta object
    delta = timedelta(days=excel_time)

    # Convert the timedelta object to a datetime object
    base_date = datetime(1900, 1, 1)
    time = base_date + delta

    # Format the datetime object as hh:mm:ss
    return time.strftime("%H:%M:%S")


